//Tipo de datos string
var variable = "Hola soy una variable";
document.write(variable);
document.write("<br>");

//Tipo de datos integer
var variable = 12;
document.write(variable);
document.write("<br>");

//Tipo de datos float, van con punto, no con coma.
var decimal = 5.83;
document.write(decimal);
document.write("<br>");

//Tipo de datos boolean, solo admiten true o false
var cierto = true;
document.write(cierto);
document.write("<br>");

//Tipo de datos hexadecimal, empiezan por 0x
var hexadecimal = 0xff0011;
document.write(hexadecimal);
document.write("<br>");

//Constantes
const cinco = 5;
document.write(cinco);
document.write("<br>");